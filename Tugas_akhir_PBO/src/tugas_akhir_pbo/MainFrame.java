    private void btnHilangkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHilangkanActionPerformed
        int row = tableClients.getSelectedRow();
        
        if(row < 0){
            JOptionPane.showMessageDialog(this,"No row is selected! ", 
                    "Select row",
                    JOptionPane.ERROR_MESSAGE);
        } else {
           DefaultTableModel model = (DefaultTableModel) tableClients.getModel();
           model.removeRow(row);
       }
    }//GEN-LAST:event_btnHilangkanActionPerformed

