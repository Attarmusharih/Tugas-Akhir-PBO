    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
       String nama = tfNama.getText();
       String nim  = tfNim.getText();
       String jurusan = tfJurusan.getText();
       String fakultas = tfFakultas.getText();
       
       if (nama.isEmpty() || nim.isEmpty() || jurusan.isEmpty()|| fakultas.isEmpty() ){
           JOptionPane.showMessageDialog(this, "Lengkapi semua data", 
                   "Coba lagi",
                   JOptionPane.ERROR_MESSAGE);
       } else {
           DefaultTableModel model = (DefaultTableModel) tableClients.getModel();
           model.addRow(new Object[]{nama, nim, jurusan, fakultas});
           
           tfNama.setText("");
           tfNim.setText("");
           tfJurusan.setText("");
           tfFakultas.setText("");
           
       }
       
    }//GEN-LAST:event_btnTambahActionPerformed

